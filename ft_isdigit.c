int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
/*
*************************************************************************
DESCRIPTION
checks digit.
*************************************************************************
RETURN
TRUE (1) || FALSE (0)
 */
