#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	while (lst)
	{
		if (!lst->next)
			return (lst);
		lst = lst->next;
	}
	return (lst);
}
/*
*************************************************************************
DESCRIPTION
find last node
*************************************************************************
RETURN
last node
 */
