#include <stdlib.h>
#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*sub;

	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	sub = malloc(len + 1);
	if (!sub)
		return (NULL);
	ft_strlcpy(sub, s + start, len + 1);
	return (sub);
}
/*
*************************************************************************
DESCRIPTION
allocated substring of size 'len' from start index of 's'
*************************************************************************
RETURN
NULL (no src, allocate fail) || "" (exceed string) || pointer to sub (success)
 */
