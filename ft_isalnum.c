#include "libft.h"

int	ft_isalnum(int c)
{
	return (ft_isalpha(c) || ft_isdigit(c));
}
/*
*************************************************************************
DESCRIPTION
checks alphabet or number.
*************************************************************************
RETURN
TRUE (1) / FALSE (0)
 */
