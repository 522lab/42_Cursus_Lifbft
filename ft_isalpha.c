int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
/*
*************************************************************************
DESCRIPTION
checks alphabet.
*************************************************************************
RETURN
TRUE (1) / FALSE (0)
 */
