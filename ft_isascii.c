int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}
/*
*************************************************************************
DESCRIPTION
checks ASCII. (decimal: 0-127)
*************************************************************************
RETURN
TRUE (1) || FALSE (0)
 */
