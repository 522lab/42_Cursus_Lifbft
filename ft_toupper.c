int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 'a' + 'A');
	return (c);
}
/*
*************************************************************************
DESCRIPTION
if lower-case, convert to upper-case
*************************************************************************
RETURN
converted ASCII || original ASCII
 */
