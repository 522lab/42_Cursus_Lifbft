int	ft_isprint(int c)
{
	if (c >= 32 && c <= 126)
		return (1);
	return (0);
}
/*
*************************************************************************
DESCRIPTION
checks printable. (decimal: 32-126)
*************************************************************************
RETURN
TRUE (1) / FALSE (0)
 */
