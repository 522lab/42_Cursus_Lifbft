#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}
/*
*************************************************************************
DESCRIPTION
delete and free a node
*************************************************************************
RETURN
none
 */
